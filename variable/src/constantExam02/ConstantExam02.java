package constantExam02;

public class ConstantExam02 {

	public static void main(String[] args) {
		// 원의 넓이 구하기 (반지름*반지름*3.14)
		//반지름:3.5
		//3.14 상수로 선언
		
		//원의 넓이=~~
		double radius = 3.5, area = 0.0;
		double pi = 3.14;
		
		area = radius*radius*pi;
				
		System.out.println("원의 넓이=" + area);

	}

}
