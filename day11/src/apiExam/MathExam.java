package apiExam;

public class MathExam {

	public static void main(String[] args) {
		double d = 4589.7687;
		
		System.out.println(Math.abs(5-9));
		System.out.println(Math.max(34, 50));
		System.out.println(Math.pow(2, 3));//2의 2제곱
		System.out.println(Math.floor(d));//소수 이하 버림
		System.out.println(Math.ceil(d));//올림
		System.out.println(Math.round(d));//반올림

	}

}
